package day3;

public class ArrayTypes {
	
	public static void main(String[] args) {
		double[] doubleArray = new double[4];
		doubleArray[0] = 1.2;
		doubleArray[1] = 2.5;
		doubleArray[2] = 4;
		doubleArray[3] = 5.4;
		
		for(int i = 0; i < doubleArray.length; i++) {
			System.out.println(doubleArray[i]);
		}
		
		
		
		
		
	}

}
