package day1;

public class ForLoopExample {
	
	public static void main(String[] args) {
		
		for(int i = 0; i <= 3; i++) {
			System.out.println(i);
		}
		
		
		//int i = 0 - initializer (starting point of the loop)
		//i <= 3 - loop condition
		//i++ - update statement
		
		/*
		 * {
			System.out.println(i);
		} - body of the loop
		 */
		
	}

}
