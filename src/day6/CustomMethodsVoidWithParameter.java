package day6;

public class CustomMethodsVoidWithParameter {
	/*
	 *   Methods:
  		- void - with parameter
	 */
	public static void main(String[] args) {
		sayHello("Asad");
		sayHello("Nuray");
	}
	
	public static void sayHello(String name) {
		System.out.println("Hello " + name);
	}

}
